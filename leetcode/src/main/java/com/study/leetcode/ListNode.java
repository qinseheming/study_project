package com.study.leetcode;

/**
 * Definition for singly-linked list.
 * 单链表
 *
 * @author : yangming
 * @date : 2019/9/17
 */
public class ListNode {

    public int val;

    public ListNode next;

    ListNode(int x) {
        val = x;
    }

    @Override
    public String toString() {
        return val + "";
    }
}
